#include <stdio.h>

/**
 * add - funtion to add two integers
 * @x - first int
 * @y - second int
 * Return: sum of two ints
 */
int add(int x, int y)
{
	return x + y;
}

/**
 * sub - function to subtract two integers
 */
int sub(int x, int y)
{
	return x - y;
}

/**
 * mul - funtion to multiply two ints
 */
int mul(int x, int y)
{
	return x * y;
}

/**
 * div - function to divide two integers
 */
int div(int x, int y)
{
	return x / y;
}

/**
 * mod - funtion to return the remainder of two integers
 */
int moc(int x, int y)
{
	return x % y;
}
